                  <h2 class="blog-post-title">Sejarah Singkat Saxophone</h2>
                   <p>Saxophone adalah instrumen yang masih tergolong dalam aerophone. biasanya terbuat dari logam dan dimainkan menggunakan single-reed seperti klarinet. Saksofon umumnya dihubungkan dengan popular music, big band music dan jazz, tetapi awalnya ditujukan sebagai instrumen orkestra dan band militer.
                    <hr>
                     <p>Teknik Dasar Bermain Saxophone Yang Benar

                  
                  <p>1. Teknik Memegang Saxophone. 
                  <p>Posisi jari tengah, jari telunjuk, dan jari manis, baik tangan kiri ataupun tangan kanan harus tepat berada pada bagian permukaan katup nada.
                  <p>2. Pernapasan Saxophone Yang Baik
                  <p>Pernapasan yang baik itu menggunakan diafragmatis. Hal ini dikarenakan volume udara yang dihasilkan dapat lebih besar dan kuat dibandingkan dengan cara pernapasan paru – paru.
                  <p>3. Posisi Bermain Saxophone
                  <p>Tidak ada larangan dalam posisi bermain saxophone, tapi usahakan anda tidak terlalu tegang dan bersikap santai saat bermain alat musik saxophone.
                  <p>4. Teknik Embosur Saxophone.
                  <p>Embosur adalah teknik mengatur posisi bibir dan gigi pada mouthpiece, dan untuk embosur yang baik, pada umumnya meletakkan gigi atas pada bagian atas dari mouthpiece.</p>
                  <p> 5. Teknik Peniupan Saxophone.
                    <p>Untuk menghasilkan suara yang indah dibutuhkan juga tenaga yang kuat dalam meniup saxophone yang diartikan dengan “meniup dengan udara panas".
