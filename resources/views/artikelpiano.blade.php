                  <h2 class="blog-post-title">Sejarah Singkat Piano</h2>
                   <p>Pianoatau pianoforte adalah instrumen musik yang diklasifikasikan sebagai instrumen perkusi yang dimainkan dengan menekan tuts-tuts pada piano. Setiap tuts tersambung ke hammer yang ada di dalam piano dan menekan senar di dalamnya, sehingga menghasilkan bunyi. Setiap senar memiliki panjang yang berbeda dan menghasilkan bunyi yang berbeda pula. Piano adalah alat musik yang dimainkan dengan jari-jemari tangan. Pemain piano disebut pianis.</p>
                  <hr>
                  <p>Terdapat 7 basic C Major Scale triad piano chords minor diminished yaitu
                    <p>- C Major
                    <p>- D Minor
                    <p>- E Minor
                    <p>- F Major
                    <p>- G Major
                    <p>- A Minor
                    <p>- B Diminished

                    <p>dan disini juga ada jenis piano loh...
                    <p>1. grand
                      <P> Ada banyak variasi ukuran piano grand yang terkadang berbeda-beda tiap merek. Namun, pada umumnya variasi ukuran piano grand yakni: 
                      <P>concert grand (berkisar antara 2,2 hingga 3 meter), parlor grand (berkisar 1,7 hingga 2,2 meter), dan baby grand (berkisar antara 1,5 meter).

                      <P>Semakin besar ukuran piano grand, semakin panjang pula senarnya. Senar yang lebih panjang akan menghasilkan bunyi yang lebih keras dan natural. Hal ini menjadikan concert grand sebagai pilihan utama untuk keperluan rekaman dan konser. Sementara piano yang berukuran lebih kecil, seperti parlor grand dan baby grand umumnya menjadi opsi lain bagi yang mempunyai dana terbatas atau ruang yang sempit.

                    <p>2. upright
                      <P>Piano upright pada umumnya lebih murah dan lebih kecil sehingga populer untuk penggunaan di rumah, gereja, sekolah, ataupun fasilitas publik. Namun, dikarenakan pendeknya senar yang menyebabkan suara kurang natural, sulitnya memposisikan mikrofon untuk keperluan rekaman, hammer yang terkadang terasa kurang natural, suara yang kurang keras, dan sempitnya ruang bagi gelombang bunyi untuk beresonansi, membuat piano upright menjadi opsi yang kurang populer untuk keperluan rekaman dan konser.

                    <p>3. elektronik dan digital
                      <P> Umumnya piano elektronik dimainkan untuk lagu bergenre pop, rock, dan jazz.
                          <P>Teknologi pengambilan sampel secara digital yang sudah semakin berkembang memungkinkan produsen piano digital piano untuk merekam berbagai variasi sampel suara piano dalam berbagai kondisi (ditekan pelan, sedang, maupun keras). Hal ini memungkinkan piano digital untuk memiliki suara natural yang menyerupai piano akustik.
