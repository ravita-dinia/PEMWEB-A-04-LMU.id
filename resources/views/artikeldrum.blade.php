                  <h2 class="blog-post-title">Sejarah Singkat Drum</h2>
                   <p>Drum adalah kelompok alat musik perkusi yang terdiri dari kulit yang direntangkan dan dipukul dengan tangan atau sebuah batang. Selain kulit, drum juga digunakan dari bahan lain, misalnya plastik. Drum terdapat di seluruh dunia dan memiliki banyak jenis, misalnya kendang, timpani, BodhrÃ¡n, Ashiko, snare drum, bass drum, tom-tom, beduk, dan lain-lain.
                  <hr>
                      <p>Teknik dasar :
                      <p>1. Teknik memegang stick dan memukul drum 
                      <p>Teknik memukul drum di dasari dari naluri dan perasaan hati. Jangan sampai pada waktu kita lagi ada masalah, langsung di lampiasakan kepada drum. Yang ada kita main drumnya tak beraturan dan bisa mengalami kerusakan pada drum tersebut hehe

                      <p>2. Memijak pedal drum dan hi hat
                      <p>Ada dua metode, yaitu: 
                        <p>- Teknik Pedal Menggunakan Ujung Kaki
                        Teknik ini sangatlah efektif jika digunakan dengan lagu yang bertempo cepat, karena power sura yang dihasilkan dengan teknik ini lebih keras. Teknik ini juga sangat bagus bagi untuk  pemula, karena tone yang dihasilkan juga lebih jelas. Begitupun pada hi-hat, kita juga akan lebih mudah untuk mengatur tempo hi-hat apabila menggunakan teknik ini.

                        <p>- Teknik Pedal Flat
                        Teknik  berikut ini dapat menghasilkan suara yang tidak terlalu keras, karena posisi kaki akan menempel pada pedal drum. Teknik ini biasa kita gunakan pada lagu â€“ lagu santai atau mellow.
                      <p>3. Teknik Open & Close Hi-hat
                      <p> Untuk Teknik berikut ini merupakan teknik dasar drum fariasi bermain hi-hat, yang pertama adalah teknik open, yaitu hi-hat dipukul dalam kondisi terbuka. Teknik ini bisa dimainkan dengan cara meletakkan kaki kiri pada pedal hi-hat, kemudian menekannya serta bersamaan dengan memukul hi-hat. Tehnik Close hi-hat, yaitu hi-hat dipukul dalam kondisi menutup. Teknik berikut ini bisa kita mainkan dengan cara meletakkan kaki kiri pada pedal sebagai tumpuan ( tanpa menekannya ), kemudian pukul hi-hat yang dalam posisi menutup.

                      <p>4. Teknik Pukulan Drum
                        <p>Jika kita ingin memainkan sebuah drum, alat music ini tidak mudah seperti yang kita bayangkan. Walaupun terlihat sangat mudah tanpa adanya grip dan not, akan tetapi jika teknik memukul drum dan pijakan pedal tidak seirama, maka suara yang dihasilkan tentunya sangat tidak nyaman jika di dengarkan. Pertama kita harus pelajari cara memegang stick dengan benar, Karen ini merupakan teknik dasar drum, bisa dilakukan denga match grip (lurus) dan juga traditional grip (menyamping). 
                        <p>Kemudian pahami ketukan dan mulailah memukul dengan tepat, berikut beberapa macam teknik ketukan dalam bermain drum :
                       <p> - Single stroke
                        <p>- Para diddle
                        <p>- Para diddle-diddle
                        <p>- Triple stroke
                        <p>- Double stroke
                        <p>- Triplet/rough 

